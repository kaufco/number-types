/**
 * A `number` that is used as a 32 bit integer.
 */
export type int = number;

/**
 * A `bigint` that is used as a 64 bit integer.
 */
export type long = bigint;

/**
 * A `number` that is used as a (_IEEE-754 Single_)[https://en.wikipedia.org/wiki/Single-precision_floating-point_format].
 */
export type float = number;

/**
 * A `number` that is used as a (_IEEE-754 Double_)[https://en.wikipedia.org/wiki/Double-precision_floating-point_format].
 */
export type double = number;
